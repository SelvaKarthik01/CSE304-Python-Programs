# To Calculate the Total Money in Piggy Bank
n_10 = int(input("How many 10 Rupee Notes are there : "))
n_50 = int(input("How many 50 Rupee  Notes are there : "))
n_100 = int(input("How many 100 Rupee Notes are there : "))
n_200 = int(input("How many 200 Rupee Notes are there : "))
sum = 10*n_10 + 50*n_50 + 100*n_100 + 200*n_200
print("The Total Amount in the Piggy Bank is : ",sum)
